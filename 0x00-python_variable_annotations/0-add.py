#!/usr/bin/env python3
"""
This module contains a function
that adds two float numbers with type annotations.
"""


def add(a: float, b: float) -> float:
    """
    Add two floats.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
